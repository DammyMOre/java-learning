package calculations;

public class NestedLoopPractice {
    public static void main(String[] args) {

        for (int column = 1; column <= 2; column++) {
            //System.out.println(column);
            for (int row = 1; row <= 3; row--) {
                System.out.print( row+" ");
            }
            System.out.println();

        }
    }
}
